def count_neighbours(grid, row, col):
  return sum([
      grid[i][j]
        for i in range(max(0, row - 1), min(row + 2, len(grid)))
        for j in range(max(0, col - 1), min(col + 2, len(grid[i])))
    ]) - grid[row][col]

